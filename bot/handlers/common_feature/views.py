        "💳 <b>Разделение чека</b>\n"
        "• /split_bill — пошагово создать событие разделения чека с публикацией и кнопками.\n"
        "• /split_bill_add &lt;id&gt; &lt;user_id&gt; — добавить участника вручную (организатор).\n"
